public class A_Calculator {
    public double add(double n1, double n2){
        return n1 + n2;
    }
    
    public double substract(double n1, double n2){
        return n1 - n2;
    }
    
    public double multiplication(double n1, double n2){
        return n1 * n2;
    }
    
    public double division(double n1, double n2){
        return n1 / n2;
    }
}
